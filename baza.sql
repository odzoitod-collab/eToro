-- 1. ПОЛЬЗОВАТЕЛИ (USERS) — ядро для бота
-- Запросы бота: get by user_id, get by referrer_id, update by user_id, insert
create table public.users (
  user_id bigint not null,
  username text null,
  full_name text null,
  photo_url text null,
  referrer_id bigint null,
  balance numeric(18, 2) not null default 0,
  worker_min_deposit numeric(18, 2) not null default 10,
  luck text not null default 'default',
  withdraw_message_type text not null default 'default',
  preferred_currency text not null default 'RUB',
  is_kyc boolean not null default false,
  web_registered boolean not null default false,
  email text null,
  country_code text null default 'RU',
  notifications_enabled boolean not null default true,
  trading_blocked boolean not null default false,
  is_worker boolean not null default false,
  last_login timestamptz null,
  login_count integer null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint users_pkey primary key (user_id),
  constraint users_luck_check check (luck in ('win', 'lose', 'default'))
);

create index idx_users_referrer on public.users (referrer_id) where referrer_id is not null;
create index idx_users_username on public.users (username) where username is not null;

-- 2. НАСТРОЙКИ (SETTINGS) — одна запись на проект, частые чтения
create table public.settings (
  id bigint generated by default as identity primary key,
  support_username text not null default 'Support_Agent',
  bank_details text null default 'Реквизиты не указаны',
  min_deposit numeric(18, 2) not null default 100,
  min_withdraw numeric(18, 2) not null default 500,
  is_tech_work boolean not null default false,
  created_at timestamptz not null default now()
);

-- Гарантированная одна строка настроек (для быстрого limit 1)
insert into public.settings (support_username, min_deposit, min_withdraw)
select 'Support_Agent', 100, 500
where not exists (select 1 from public.settings limit 1);

-- Миграция: добавить min_withdraw, если колонки нет (для существующих БД)
alter table public.settings add column if not exists min_withdraw numeric(18, 2) not null default 500;

-- 3. РЕКВИЗИТЫ ПО СТРАНАМ — справочник, запросы: по is_active + order by country_name
create table public.country_bank_details (
  id bigint generated by default as identity primary key,
  country_name text not null unique,
  country_code text not null,
  currency text not null,
  bank_details text not null,
  exchange_rate numeric(18, 6) not null default 1,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index idx_country_active_name on public.country_bank_details (is_active, country_name)
  where is_active = true;

-- Реквизиты только по РФ: одна запись «Россия» по умолчанию
