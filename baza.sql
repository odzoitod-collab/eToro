-- 4. ШАБЛОНЫ СООБЩЕНИЙ ВЫВОДА — справочник, запрос: is_active order sort_order
create table public.withdraw_message_templates (
  id bigint generated by default as identity primary key,
  message_type text not null unique,
  title text not null,
  description text not null,
  icon text null default '⚠️',
  button_text text null,
  is_active boolean not null default true,
  sort_order int not null default 0,
  created_at timestamptz not null default now()
);

create index idx_withdraw_active_sort on public.withdraw_message_templates (is_active, sort_order)
  where is_active = true;

-- 5. ЗАЯВКИ НА ДЕПОЗИТ — частые запросы: по worker_id + status = pending, по id
create table public.deposit_requests (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users (user_id) on delete cascade,
  worker_id bigint null references public.users (user_id) on delete set null,
  amount_local numeric(18, 2) not null,
  amount_usd numeric(18, 2) not null,
  currency text not null,
  method text not null,
  status text not null default 'pending',
  created_at timestamptz not null default now(),
  processed_at timestamptz null,
  constraint deposit_requests_status_check check (status in ('pending', 'approved', 'rejected'))
);

-- Критично для бота: быстрый список pending по воркеру
create index idx_deposits_worker_pending on public.deposit_requests (worker_id, created_at desc)
  where status = 'pending';
create index idx_deposits_user on public.deposit_requests (user_id, created_at desc);

-- 6. СДЕЛКИ (TRADES) — веб-приложение
create table public.trades (
  id text not null default gen_random_uuid()::text primary key,
  user_id bigint not null references public.users (user_id) on delete cascade,
  pair text not null,
  symbol text not null,
  type text not null check (type in ('Long', 'Short')),
  amount numeric(18, 2) not null,
  leverage int null default 1,
  entry_price numeric(18, 8) not null,
  final_price numeric(18, 8) null,
  start_time bigint not null,
  duration_seconds int not null,
  status text not null default 'active' check (status in ('active', 'completed', 'cancelled')),
  final_pnl numeric(18, 2) null,
  is_winning boolean null,
  forced_result text null check (forced_result is null or forced_result in ('win', 'lose')),
  created_at timestamptz not null default now()
);

create index idx_trades_user_status on public.trades (user_id, status);
create index idx_trades_created on public.trades (created_at desc);

