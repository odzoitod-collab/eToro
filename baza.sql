-- =============================================================================
-- ТРИГГЕРЫ: updated_at и лог изменений
-- =============================================================================

-- Универсальная функция: обновить updated_at
create or replace function set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

-- users: updated_at + лог изменений баланса/реферера
create or replace function log_mammoth_changes()
returns trigger language plpgsql as $$
begin
  new.updated_at := now();
  if old.referrer_id is distinct from new.referrer_id then
    insert into mammoth_change_log (worker_id, mammoth_id, change_type, old_value, new_value)
    values (
      coalesce(new.referrer_id, old.referrer_id),
      new.user_id,
      'referrer',
      old.referrer_id::text,
      new.referrer_id::text
    );
  end if;
  if old.balance is distinct from new.balance and new.referrer_id is not null then
    insert into mammoth_change_log (worker_id, mammoth_id, change_type, old_value, new_value)
    values (new.referrer_id, new.user_id, 'balance', old.balance::text, new.balance::text);
  end if;
  return new;
end;
$$;

create trigger trigger_users_updated
  before update on public.users for each row execute function log_mammoth_changes();

-- country_bank_details: updated_at при обновлении
create trigger trigger_country_updated
  before update on public.country_bank_details for each row execute function set_updated_at();

-- worker_notification_settings: updated_at при обновлении
create trigger trigger_worker_notif_updated
  before update on public.worker_notification_settings for each row execute function set_updated_at();

-- automation_rules: updated_at при обновлении
create trigger trigger_automation_updated
  before update on public.automation_rules for each row execute function set_updated_at();

-- mammoth_notes: updated_at при обновлении
create trigger trigger_notes_updated
  before update on public.mammoth_notes for each row execute function set_updated_at();

